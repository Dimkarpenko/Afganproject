            <h1 style="text-shadow:#fff 0 0 20px;margin-top:10%">О пректе</h1>
            <h2 style="color:#fff;width:70%"><i>Памятник воинам-афганцам находится на улице Республики у дома № 152 города Тюмени. Монумент расположен у здания Тюменского Колледжа Искусств. Торжественное открытие памятника состоялось 2 ноября 2002 года на площади имени Губкина, на месте, где 15 февраля 1999 года был установлен памятный гранитный камень в честь тюменцев, погибших в Афганистане при исполнении воинского долга.<br><br>
                Наша работа представляет собой макет модернизации монумента “Памяти павшим в войне в
                Афганистане” и благоустройство территории вокруг него. В настоящий момент в Тюмени уже есть 
                памятник “Воинам - афганцам” на пересечении улиц Геологоразведчиков и Республики. Мы
                решили его немного изменить, оставив старый монумент, и добавив к нему новый, более
                интересный. Наш макет выполнен в масштабе 1/72, мы посчитали, что это будет наиболее
                оптимальный размер. В период 1979 – 1990 г.г. в Афганистане проводились боевые действия с
                применением различной техники. Помимо нового монумента мы решили добавить постамент
                с танком Т-55, представленном выше, который принимал непосредственное участие в этом конфликте.На макете этот
                танк представлен моделью. Какой же парк без деревьев? Деревья на нашей диораме сделаны
                из сухих растений (сорняков и д.р.), а крона из измельченного поролона. Сам монумент, как и
                основание сделаны из пеноплекса и полистерола. Раз есть деревья, значит должен быть и
                газон, он сделан из пучков флока, которые установлены на специальное основание.</i></h2>
