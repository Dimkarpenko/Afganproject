            <h1 style="text-shadow:#fff 0 0 20px;margin-top:10%">About the project</h1>
            <h2 style="color:#fff;width:70%"><i>The monument to Afghan soldiers is located on Respublika Street near house number 152 in Tyumen. The monument is located near the building of the Tyumen College of Arts. The grand opening of the monument took place on November 2, 2002, on Gubkin Square, on the site where a memorial granite stone was erected on February 15, 1999 in honor of the Tyumen people who died in Afghanistan while on military duty.<br><br>
                Our work is a model of the modernization of the monument "In memory of the fallen in the war in
                Afghanistan ‚Äùand improvement of the territory around it. At the moment, Tyumen already has
                Monument to Warriors - Afghans at the intersection of Geologorazvedchikov and Republic streets. We
                decided to change it a little, leaving the old monument, and adding a new, more
                interesting. Our model is made in 1/72 scale, we thought it would be the most
                optimal size. In the period 1979 - 1990 in Afghanistan, hostilities were conducted with
                the use of various techniques. In addition to the new monument, we decided to add a pedestal
                with the T-55 tank shown above, which was directly involved in this conflict.
                the tank is represented by the model. What is a park without trees? The trees in our diorama are made
                from dry plants (weeds, etc.), and the crown from crushed foam rubber. The monument itself, like
                the base is made of polystyrene foam and polystyrene. Since there are trees, then there must be
                lawn, it is made of flock bunches, which are installed on a special base.</i></h2>
